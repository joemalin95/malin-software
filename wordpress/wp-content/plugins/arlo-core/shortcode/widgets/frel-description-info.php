	protected function render() {
		$title = get_bloginfo( 'name' );

		if ( empty( $title ) )
			return;

		
		$settings 	= $this->get_settings();
		
		// ****************************** Container ***********************************
		$fnContainer	= $settings['fn_container'];								//*
		$fnContCustom	= $settings['fn_container_custom'];							//*
		if($fnContainer == 'container'){											//*
			$fnC_Start	= '<div class="container fn_container '.$fnContCustom.'">';	//*
			$fnC_End	= '</div>';													//*
		}else{																		//*
			$fnC_Start 	= $fnC_End = '';											//*
		}																			//*
		// ****************************************************************************
		
		
		$subtitle 	= $settings['subtitle'];
		$title 		= $settings['title'];
		$desc 		= $settings['desc'];
		
		if($subtitle != ''){
			$subtitle = '<h5>'.$subtitle.'</h5>';
		}
		if($title != ''){
			$title = '<h3>'.$title.'</h3>';
		}
		if($subtitle != '' || $title != ''){
			$title = '<div class="title_holder">'.$subtitle.$title.'</div>';
		}
		if($desc != ''){
			$desc = '<div class="desc_holder">'.$desc.'</div>';
		}
		
		$html = Frel_Helper::frel_open_wrap();
		$html .= '<div class="fn_cs_desc_info">'.$fnC_Start.$title.$desc.$fnC_End.'</div>';
		$html .= Frel_Helper::frel_close_wrap();
		
		echo $html;
	}

